// Build cumulative vocab sets: HSK N includes all words from HSK 1 through N
const HSK_CUMULATIVE = {};
(function() {
    let cumulative = new Set();
    for (let level = 1; level <= 4; level++) {
        for (const word of HSK_VOCAB[level]) {
            cumulative.add(word);
        }
        HSK_CUMULATIVE[level] = new Set(cumulative);
    }
})();

// Check if a word is above the given HSK level
function isAboveLevel(word, hskLevel) {
    if (!hskLevel || !HSK_CUMULATIVE[hskLevel]) return false;
    return !HSK_CUMULATIVE[hskLevel].has(word);
}

